public class BillModel {
	private String friendName;//消费对象姓名
	private String friendPhone;//消费对象电话
	private String money;//消费金额
	private String time;//消费时间
	private String type;//消费类型  （0：按周计费，1：按月计费，2：按年计费，3：短信）
	private int friendType;//（0：非APP用户，1：android<APP用户>，2 ：ios<APP用户>）
	private String process;//     业务状态（0：已扣款，1：处理中，2：已退款）
	
	
	
	public String getProcess() {
		return process;
	}
	public void setProcess(String process) {
		this.process = process;
	}
	public int getFriendType() {
		return friendType;
	}
	public void setFriendType(int friendType) {
		this.friendType = friendType;
	}
	public String getFriendName() {
		return friendName;
	}
	public void setFriendName(String friendName) {
		this.friendName = friendName;
	}
	public String getFriendPhone() {
		return friendPhone;
	}
	public void setFriendPhone(String friendPhone) {
		this.friendPhone = friendPhone;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
}
